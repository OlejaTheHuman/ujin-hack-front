function BiaxialBarChart({data1, data2}: {data1: number[], data2: number[]}) {
    return (
        <BarChart
            width={1000}
            height={600}
            series={[
                {
                    data: data1 as [],
                    label: 'Расход Воды (куб. м.)',
                    id: 'pvId',
                    yAxisKey: 'leftAxisId',
                    color: 'lightblue',
                },
                {
                    data: data2 as [],
                    label: 'Расход Электричества (кВт.ч.)',
                    id: 'uvId',
                    color: 'orange',
                    yAxisKey: 'rightAxisId',
                },
            ]}
            xAxis={[{ data: xLabels, scaleType: 'band' }]}
            yAxis={[{ id: 'leftAxisId' }, { id: 'rightAxisId' }]}
            rightAxis="rightAxisId"
        />
    );
}




